// * While Solution
// ? While Num is bigger or equal to 1 the loop will continue and Inside of the loop it will print That Num every time and it will decrement it by 1 every time.
function NumTo1(num) {
  while (num >= 1) {
    console.log(num);
    num--;
  }
}
